		<p>Before starting the days travel we drove along the rim road of the Canyon de Chelly, stopping at
			a number of the numerous overlooks. The Canyon de Chelly is only 500-600 feet deep, but spectacular,
			with many impressive sandstone structures. From some of the overlooks we had glimpses
			of Anastasi ruins built into the canyon wall. The floor of the canyon
			showed signs of recent agriculture, and habitation as well as a vehicle track that we understood
			is used for guided jeep tours.</p>
