		<p>So what is OX11?</p>
			<p>As a starter have a look at thier website <a href="http://www.overlandexpo.com">OverlandExpo</a>.</p>
			<p>As there slogan says, this is the place to <em>get outfitted, get trained and get inspired ..</em>. As
			you might gather from that slogan alone, this is an event where people gather to talk about traveling the world
			by motorcycle, or some other type of vehicle. The attendees are either travelers, suppliers or people just looking
			to find out what and how to do travel.</p>
			<?php Skin::JournalGalleryThumbnails($trip, $entry); ?><p>The first thing to strike us was the range of vehicles. We were assigned a parking place in the exhibitors
			area (due to our recent journey we got classified as travelers). Around us there were all manner
			of vehicles, ranging from the hughmungus UNICAT truck, through a range  of other 
			trucks down through campers to relatively modest Landrovers and Landcruisers. Just to prove
			that size does not necessarily correlate with achievement I would like to mention
			one couple who have traveled around the world in a vehicle no bigger than a typical
			American SUV. <a href="http://www.4x4x4continentes.com">4x4x4continentes</a>  </p>
			<p>There was also a sizable contingent of motorcyclists (mostly solo), whose exploits covered 
			the gamut of travel, Mexico's Baha Peninsula, Africa, Mongolia, Russia, and around the world.</p>
			<p>The expo lasted three days with each day being jam packed with a  dozen or more parallel streams
			of 1 hour sessions covering a breathtaking range of topics, from "how to survive as a couple", through
			fixing tires, storing food, cooking gourmet meals over a camp stove, crossing borders, and shipping
			yourd transport to another country.</p>
			<p>In parallel with this there was a movie festival that presented some truly memorable movies.</p>
			<ul><li><p>One called the <em>Headless Valley</em> was shot on 16mm film in the 1950's by a Canadian couple
			who spent their summer traveling up a wild River in BC to a remote valley that was infamous (at the time)
			as being a place where many murders took place.</p></li>
			<li><p>We saw a film of a 1975 overland journey from the UK to Katmandu through Iran and Afghanistan by a group
			of young people in the back of an old Bedford truck.</p></li>
			<li><p>Nina saw, and was captivated by, Ted Simon's film about his 1970 around the world trip by motorcycle, 
			and his repeat journey 27 years later at the age of 69. I am sure we will be buying a copy of 
			<em>Jupiter's Travels</em> Ted's book about his original journey that sold 400,000 copies.</p></li>
			<li><p>I saw an impressive presentation and slide show by Lois Pryce about her 2006 solo journey
			from London to Capetown (Red Tape and White Knuckles). It's hard to credit the smile on her face 
			as she tells about traveling on a flat bed railway car with a group of drunk and stoned Angolan soldiers.</p></li>
			</ul><p>However, we most enjoyed talking to the dozens of people that dropped by to look at our Tiger
			and ask questions about our recent journey. I only hope I did not overload them
			with my enthusiasm.</p>
